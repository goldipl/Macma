            <p>Doming (naklejka żywiczna) to technologia znakowania pozwalająca na uzyskanie efektu niemal trójwymiarowego obrazu. Polega na nałożeniu na kolorowy wydruk cyfrowy warstwy żywicy poliuretanowej, która dodaje kolorom wyrazistości i głębi obrazu. Warstwa żywicy ma także funkcje ochronne - logo zachowuje swoją barwę i jest odporne na uszkodzenia. Efekt końcowy jest bardzo estetyczny i efektowny.</p>
            <p>Podczas znakowania gadżetów metodą domingu możemy posłużyć się plikami wektorowymi lub skorzystać z wysokiej jakości map bitowych (rozdzielczość nie mniejsza niż 300 dpi) przy zachowaniu oryginalnych rozmiarów znakowania. Drukarki cyfrowe bazują na kolorach CMYK, dlatego też kolory PANTONE, HKS, RGB i inne zostaną zamienione automatycznie na paletę CMYK, co może spowodować drobne różnice w barwach, na które nie mamy wpływu. Folia pod doming wycinana jest przy użyciu plotera tnącego, dlatego też trzeba pamiętać o minimalnych przesunięciach noża podczas wycinania. Jeżeli chcemy uniknąć widoku białego tła wynikającego z koloru folii konieczne jest uwzględnienie 2 mm spadów w projekcie.</p>
